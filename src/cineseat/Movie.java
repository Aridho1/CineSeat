package cineseat;

/**
 *
 * @author rahma
 */
public class Movie {

    private String title;
    private int price;

    public Movie(String title, int price) {
        this.title = title;
        this.price = price;
    }

    public String getTitle() {
        return title;
    }

    public int getPrice() {
        return price;
    }

    @Override
    public String toString() {
        return title;
    }
}
